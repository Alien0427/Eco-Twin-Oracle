class PrescriptionResponse(BaseModel):
    """
    Pydantic response schema for a validated prescriptive recommendation.
    Mirrors the analytics_engine.Prescription dataclass for Swagger generation.
    """
    batch_id: str = Field(..., description="Batch identifier, e.g. T060")
    time_minutes: int = Field(..., ge=0, description="Elapsed minutes in batch lifecycle")
    dfa_state: str = Field(..., description="Current DFA phase name")
    bmu_index: list[int] = Field(..., description="[row, col] coordinates of the Best Matching Unit on the SOM lattice")
    bmu_distance: float = Field(..., ge=0.0, description="Euclidean distance from live vector to BMU (lower = closer to Golden Signature)")
    anomaly_class: str = Field(..., description="LVQ anomaly classification label")
    parameter_recommendations: dict[str, float] = Field(
        ..., description="DFA-validated parameter deltas: keys are telemetry fields, values are suggested adjustment magnitudes"
    )
    dfa_guardrail_passed: bool = Field(..., description="True if no parameters were blocked by the DFA transition matrix")
    blocked_parameters: list[str] = Field(
        default_factory=list,
        description="Parameters the DFA guardrail rejected as chronologically impossible"
    )

    @classmethod
    def from_dataclass(cls, p: Prescription) -> "PrescriptionResponse":
        return cls(
            batch_id=p.batch_id,
            time_minutes=p.time_minutes,
            dfa_state=p.dfa_state,
            bmu_index=list(p.bmu_index),
            bmu_distance=p.bmu_distance,
            anomaly_class=p.anomaly_class,
            parameter_recommendations=p.parameter_recommendations,
            dfa_guardrail_passed=p.dfa_guardrail_passed,
            blocked_parameters=p.blocked_parameters,
        )


class PhantomAlertResponse(BaseModel):
    """Pydantic mirror of the PhantomEnergyAlert dataclass for JSON serialisation."""
    batch_id: str
    time_minutes: int
    dfa_state: str
    motor_speed_rpm: int
    power_consumption_kw: float

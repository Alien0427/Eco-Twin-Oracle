"""
main.py
=======
Phase F: Eco-Twin Oracle — FastAPI Interoperability Layer

Exposes the full Oracle pipeline via:
  • WebSocket  /ws/live-batch/{batch_id}   — real-time streaming inference
  • REST GET   /api/v1/active-prescriptions — latest validated setpoints (Swagger)
  • REST GET   /api/v1/dfa-state            — current DFA state for any active batch
  • REST GET   /health                      — liveness probe

Design notes
────────────
• The SOM and LVQ are trained ONCE during server startup using data from all
  available batch sheets, via FastAPI's async lifespan context manager.
  A dummy initialisation fallback guarantees the server starts even if the
  Excel file is absent (e.g., CI environment).
• All analytics objects (SOM, LVQ, DFA) are stored on app.state so they are
  accessible from any endpoint without globals or module-level side-effects.
• Streaming WebSocket payload is a structured JSON envelope.
• The /docs Swagger UI is auto-generated by FastAPI from Pydantic response models.
"""

